function Config() {
  "use strict";
}

function setXAPIData(data){
	Config.endpoint = data.EndPointUrl;
	Config.authUser = data.AuthUserKey;
	Config.authPassword = data.AuthSecretKey;
	if(data.patronName==null)
		Config.actor = { "mbox":'guest@experizer.com', "name": 'Guest' };
	else
		Config.actor = { "mbox":data.Email, "name": data.patronName };
	Config.registration = "<registration-uuid>";
}
